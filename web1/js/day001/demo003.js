/*
循环语句:for,while,do while

循环输出10个数字
*/
//for 循环
for(var i=1;i<=10;i++){
    document.write(i+"&nbsp");
}

document.write("</br>");

//while 循环
var a = 1;
while(a <= 10){
    document.write(a+"&nbsp");
    a++;
}

document.write("</br>");
//do while循环
var b = 1;
do{
    document.write(b+"&nbsp");
    b++;
}while(b<=10);



